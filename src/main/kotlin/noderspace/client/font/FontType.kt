package noderspace.client.font

/**
 * Enumeration representing different font types.
 *
 * @property ExtraLight Represents the extra light font type.
 * @property Light Represents the light font type.
 * @property Regular Represents the regular font type.
 * @property Medium Represents the medium font type.
 * @property Bold Represents the bold font type.
 * @property SemiBold Represents the semi-bold font type.
 * @property ExtraBold Represents the extra-bold font type.
 * @property Black Represents the black font type.
 */
enum class FontType {

    ExtraLight, Light, Regular, Medium, Bold, SemiBold, ExtraBold, Black
}